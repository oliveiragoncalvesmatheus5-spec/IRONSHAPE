/**
 * Traduz o nome do exercício para inglês antes de buscar na API.
 * Remove acentos e ignora maiúsculas/minúsculas na comparação.
 */
function normalize(str) {
  return str
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .trim();
}

export function translateExerciseName(namePt) {
  const key = normalize(namePt);

  for (const [ptKey, enVal] of Object.entries(exerciseTranslations)) {
    if (normalize(ptKey) === key) return enVal;
  }

  return namePt;
}
